def main():
    num = [3, 4, 7, 8, 12, 15, 16]
    count = 0
    for i in num:
        if i % 2 == 0:
            count += 1
    print("The total number of even number in the list(num) = ", count)

    

if __name__ == "__main__":
    main()
    
