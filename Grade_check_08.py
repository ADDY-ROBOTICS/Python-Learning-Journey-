def get_letter_grade(alpha):
    # Code Starts
    if alpha == 1:
        print('A')
    elif alpha == 2:
        print('B')
    elif alpha == 3:
        print('C')
    elif alpha == 4:
        print('D')
    elif alpha == 5:
        print('F')
    else:
        print('Invalid grade')
    # Code Ends

# Predefined inputs
grade = 1

# Function call
get_letter_grade(grade)