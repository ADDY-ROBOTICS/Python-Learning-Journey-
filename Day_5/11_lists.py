#Write Python code to concatenate two lists, fruits containing "apple", "banana" and veggies containing "carrot", "potato", into a new list final and print it.
def main():
   fruits = ['apple', 'banana']
   veggies = ['carrot', 'potato']
   eatables = fruits + veggies 
   print(eatables)

if __name__ == "__main__":
    main()
    